    public function store(Request $request): RedirectResponse 
    {
        $validated = $request->validate([
            'message' => 'required|string|max:255',
        ]);

        /* We're then creating a record that will belong to the logged in user
           by leveraging a chirps relationship.
           We define that relationship in app/Models/User.php  */
        $request->user()->chirps()->create($validated);

        /*  using Inertia we return a redirect response to instruct Inertia to reload our chirps.index route */
        return redirect(route('chirps.index'));
    }

